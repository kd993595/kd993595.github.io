class CircleCollider{
    constructor(start_x, start_y, allpoints, borderCircles, duration){
        this.start_x = start_x;
        this.start_y = start_y;
        this.pos_x = start_x;
        this.pos_y = start_y;
        this.collided = false;
        this.outerRadius = 300/2 - 7.5;
        this.dir_x = 0;
        this.dir_y = 0;
        this.speed = .1;
        this.points = allpoints;
        this.borderCircle = borderCircles;
        this.index = 0;
        this.previousx = 0;
        this.previousy = 0;
        this.endx = 0;
        this.endy = 0;
        this.elapsed = 0;
        this.totalTime = duration;
        this.previousNumber = -1;
        this.drawBorderCircle = [];
    }

    drawCirclePhysics(){
        fill(100,100,255);
        stroke(100,100,255);
        circle(this.pos_x, this.pos_y, 15);
        this.pos_x += this.dir_x * this.speed * deltaTime;
        this.pos_y += this.dir_y * this.speed * deltaTime;
        let deltaDist = sqrt(sq(this.pos_x - this.start_x) + sq(this.pos_y - this.start_y));
        if(deltaDist > this.outerRadius){
            let degree = atan2(this.pos_x - this.start_x, this.pos_y - this.start_y);
            let normx = this.pos_x - this.start_x;
            let normy = this.pos_y - this.start_y;
            let magnitude = sqrt(sq(normx) + sq(normy));
            normx = normx / magnitude;
            normy = normy / magnitude;
            let dot_product = 2*(normx * this.dir_x + normy * this.dir_y);
            let newx = this.dir_x - dot_product * normx;
            let newy = this.dir_y - dot_product * normy;
            this.dir_x = newx;
            this.dir_y = newy;
            console.log("Collision degrees: ", degree, "dir x:", this.dir_x, "dir y:", this.dir_y);
            this.points.push({x: this.pos_x, y: this.pos_y});
            if(this.points.length == 24){
                console.log(this.points);
            }
        }
    }

    drawCircle(number){
        fill('gold');
        stroke('gold');
        circle(this.pos_x, this.pos_y, 15);
        this.elapsed += deltaTime;
        if(this.previousNumber != number){
            this.index = number % this.points.length;
            this.previousx = this.pos_x;
            this.previousy = this.pos_y;
            this.endx = this.points[this.index].x;
            this.endy = this.points[this.index].y;
            this.previousNumber = number;
            this.elapsed = 0;

            this.drawBorderCircle = [];
            for(let i=0;i<this.borderCircle.length && i<number; i++){
                this.drawBorderCircle.push(this.borderCircle[i]);
            }
        }        
        this.pos_x = lerp(this.previousx,this.endx, min(1, this.elapsed / this.totalTime));
        this.pos_y = lerp(this.previousy,this.endy, min(1, this.elapsed / this.totalTime));
        for(let i=0;i<this.drawBorderCircle.length;i++){
            fill(this.drawBorderCircle[i].c);
            stroke(this.drawBorderCircle[i].c);
            circle(this.drawBorderCircle[i].x, this.drawBorderCircle[i].y, 10);
        }
    }
}

